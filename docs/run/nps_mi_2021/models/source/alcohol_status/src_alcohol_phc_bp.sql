

  create or replace view `nps-omop-project`.`C_SREDH_NPS_MI_STAGING_SIMPLIFIED_2021_VIEWS_ONLY`.`src_alcohol_phc_bp`
  OPTIONS()
  as 

with alcohol_phc_bp as (
select
CAST(lasb.HUB_SITE_PATIENT_SK AS STRING) AS PATIENT_NUMBER,
CAST(hasb.SITE_ID AS STRING) AS SITE_ID,
NULL AS PROVIDER_ID,
CAST(hasb.SOURCE_SYSTEM AS STRING) AS SOURCE_SYSTEM,
CAST("SAT_SITE_PATIENT_ALCOHOL_STATUS_PHC_BP_OMOP" AS STRING) AS SOURCE_TABLE,
CAST(hasb.RECORD_ID AS STRING) AS RECORD_ID,
CAST(sspaspb.RECORD_STATUS AS STRING) AS RECORD_STATUS,
CAST(sspaspb.ALCOHOL_CODE AS STRING) AS ALCOHOL_CODE,
CAST(CASE
		WHEN sspaspb.ALCOHOL_CODE = 1
		THEN 'Nil'
		WHEN sspaspb.ALCOHOL_CODE = 2
		THEN 'Ocassional'
		WHEN sspaspb.ALCOHOL_CODE = 3
		THEN 'Moderate'
		WHEN sspaspb.ALCOHOL_CODE = 4
		THEN 'Heavy'
		ELSE
		null
		END AS STRING) AS ALCOHOL_STATUS_NAME,
CAST(sspaspb.PAST_ALCOHOL_LEVEL as STRING) AS PAST_ALCOHOL_CODE,
CAST(sspaspb.DRINKS_PER_DAY AS INT) AS DRINKS_PER_DAY,
CAST(sspaspb.DAYS_PER_WEEK AS INT) AS DAYS_PER_WEEK,
CAST(null AS STRING) AS ALCOHOL_ASSESSMENT_DATE,
CAST(sspaspb.YEAR_STARTED AS STRING) AS YEAR_STARTED,
CAST(sspaspb.YEAR_STOPPED AS STRING) AS YEAR_STOPPED,
CAST(sspaspb.PAST_DRINKS_PER_DAY AS STRING) AS PAST_DRINKS_PER_DAY,
CAST(sspaspb.PAST_DAYS_PER_WEEK AS STRING) AS PAST_DAYS_PER_WEEK,
CAST(sspaspb.CREATED_DATETIME AS TIMESTAMP) AS CREATED_DATETIME, 
CAST(sspaspb.UPDATED_DATETIME AS TIMESTAMP) AS UPDATED_DATETIME,
CAST(sspaspb.OMD_EFFECTIVE_DATETIME AS TIMESTAMP) AS OMD_EFFECTIVE_DATETIME,
CAST(sspaspb.OMD_SOURCE_ROW_ID AS INT) AS OMD_SOURCE_ROW_ID,
CAST(sspaspb.OMD_INSERT_MODULE_INSTANCE_ID AS INT) AS OMD_INSERT_MODULE_INSTANCE_ID,
CAST(sspaspb.OMD_EXPIRY_DATETIME AS TIMESTAMP) AS OMD_EXPIRY_DATETIME,
CAST(sspaspb.OMD_CURRENT_RECORD_INDICATOR AS STRING) AS OMD_CURRENT_RECORD_INDICATOR,
CAST(sspaspb.OMD_DELETED_RECORD_INDICATOR AS STRING) AS OMD_DELETED_RECORD_INDICATOR,
CAST(sspaspb.OMD_RECORD_SOURCE_ID AS INT) AS OMD_RECORD_SOURCE_ID,
CAST(sspaspb.OMD_HASH_FULL_RECORD AS STRING) AS OMD_HASH_FULL_RECORD
from `nps-omop-project`.`B_SREDH_NPS_MI_SF_RDV_BDV_2021`.`HUB_SITE_PATIENT_ALCOHOL_STATUS_BP_OMOP` hasb
inner join
`nps-omop-project`.`B_SREDH_NPS_MI_SF_RDV_BDV_2021`.`SAT_SITE_PATIENT_ALCOHOL_STATUS_PHC_BP_OMOP` sspaspb
on hasb.HUB_SITE_PATIENT_ALCOHOL_STATUS_BP_SK = sspaspb.HUB_SITE_PATIENT_ALCOHOL_STATUS_BP_SK
inner join
`nps-omop-project`.`B_SREDH_NPS_MI_SF_RDV_BDV_2021`.`LNK_SITE_PATIENT_ALCOHOL_STATUS_BP_OMOP` lasb
on hasb.HUB_SITE_PATIENT_ALCOHOL_STATUS_BP_SK = lasb.HUB_SITE_PATIENT_ALCOHOL_STATUS_BP_SK
inner join
`nps-omop-project`.`B_SREDH_NPS_MI_SF_RDV_BDV_2021`.`LSAT_SITE_PATIENT_ALCOHOL_STATUS_BP_OMOP` lsasb
on lasb.LNK_SITE_PATIENT_ALCOHOL_STATUS_BP_SK = lsasb.LNK_SITE_PATIENT_ALCOHOL_STATUS_BP_SK
inner join
`nps-omop-project`.`B_SREDH_NPS_MI_SF_RDV_BDV_2021`.`HUB_SITE_PATIENT_OMOP` hsp
ON lasb.HUB_SITE_PATIENT_SK = hsp.HUB_SITE_PATIENT_SK
inner join
`nps-omop-project`.`B_SREDH_NPS_MI_SF_RDV_BDV_2021`.`SAT_SITE_PATIENT_PHC_BP_OMOP` ssp
ON hsp.HUB_SITE_PATIENT_SK = ssp.HUB_SITE_PATIENT_SK
WHERE
	sspaspb.OMD_CURRENT_RECORD_INDICATOR = 'Y'
	AND sspaspb.OMD_DELETED_RECORD_INDICATOR = 'N'
	AND lsasb.OMD_CURRENT_RECORD_INDICATOR = 'Y'
	AND lsasb.OMD_DELETED_RECORD_INDICATOR = 'N'
	AND ssp.OMD_CURRENT_RECORD_INDICATOR = 'Y'
	AND ssp.OMD_DELETED_RECORD_INDICATOR = 'N'

)

select * from alcohol_phc_bp;

