

  create or replace view `nps-omop-project`.`C_SREDH_NPS_MI_STAGING_SIMPLIFIED_2021`.`src_billing_service_md`
  OPTIONS()
  as 
with billing_service_md as (
select
CAST(lbsp.HUB_SITE_PATIENT_SK AS STRING) AS PATIENT_NUMBER,
CAST(hb.SITE_ID AS STRING) AS SITE_ID , 
CAST(hb.SOURCE_SYSTEM AS STRING) AS SOURCE_SYSTEM, 
'SAT_BILLING_SERVICE_MD_OMOP' AS SOURCE_TABLE, 
CAST(NULL AS STRING) AS PROVIDER_ID, 
CAST(hb.BILLING_SERVICE_ID AS STRING) as SERVICE_ID,
CAST(sbdvbs.BILLING_SERVICE_DATE AS TIMESTAMP) AS SERVICE_DATETIME,
CAST(sbsm.ITEM_NO AS STRING) AS ITEM_NUMBER, 
CAST(sbdvbs.BILLING_SERVICE_PATIENT_COUNT AS INT) AS SERVICE_PATIENT_COUNT,
CAST(NULL AS STRING) as SERVICE_RECORD_STATUS,
CAST(sbdvbs.BILLING_SERVICE_VISIT_DATE AS TIMESTAMP) as VISIT_DATETIME,
CAST(null AS TIMESTAMP) AS CREATED_DATETIME,
CAST(null AS TIMESTAMP) AS UPDATED_DATETIME,
CAST(sbsm.OMD_EFFECTIVE_DATETIME AS TIMESTAMP) AS OMD_EFFECTIVE_DATETIME,
CAST(sbsm.OMD_SOURCE_ROW_ID AS INT) AS OMD_SOURCE_ROW_ID,
CAST(sbsm.OMD_INSERT_MODULE_INSTANCE_ID AS INT) AS OMD_INSERT_MODULE_INSTANCE_ID,
CAST(sbsm.OMD_EXPIRY_DATETIME AS TIMESTAMP) AS OMD_EXPIRY_DATETIME,
CAST(sbsm.OMD_CURRENT_RECORD_INDICATOR AS STRING) AS OMD_CURRENT_RECORD_INDICATOR,
CAST(sbsm.OMD_DELETED_RECORD_INDICATOR AS STRING) AS OMD_DELETED_RECORD_INDICATOR,
CAST(sbsm.OMD_RECORD_SOURCE_ID AS INT) AS OMD_RECORD_SOURCE_ID,
CAST(sbsm.OMD_HASH_FULL_RECORD AS STRING) AS OMD_HASH_FULL_RECORD
from 
`nps-omop-project`.`B_SREDH_NPS_MI_SF_RDV_BDV_2021`.`HUB_BILLING_SERVICE_OMOP` hb
inner join
`nps-omop-project`.`B_SREDH_NPS_MI_SF_RDV_BDV_2021`.`SAT_BILLING_SERVICE_MD_OMOP` sbsm
on hb.HUB_BILLING_SERVICE_SK = sbsm.HUB_BILLING_SERVICE_SK
inner join 
`nps-omop-project`.`B_SREDH_NPS_MI_SF_RDV_BDV_2021`.`SAT_BDV_BILLING_SERVICE_OMOP` sbdvbs
on hb.HUB_BILLING_SERVICE_SK = sbdvbs.HUB_BILLING_SERVICE_SK
inner join
`nps-omop-project`.`B_SREDH_NPS_MI_SF_RDV_BDV_2021`.`LNK_BDV_BILLING_SERVICE_OMOP` lbsp
on hb.HUB_BILLING_SERVICE_SK=lbsp.HUB_BILLING_SERVICE_SK
inner join
`nps-omop-project`.`B_SREDH_NPS_MI_SF_RDV_BDV_2021`.`LSAT_BDV_BILLING_SERVICE_OMOP` lsbsp 	
ON lbsp.LNK_BDV_BILLING_SERVICE_SK = lsbsp.LNK_BDV_BILLING_SERVICE_SK
inner join
`nps-omop-project`.`B_SREDH_NPS_MI_SF_RDV_BDV_2021`.`HUB_SITE_PATIENT_OMOP` hsp
ON lbsp.HUB_SITE_PATIENT_SK = hsp.HUB_SITE_PATIENT_SK
inner join
`nps-omop-project`.`B_SREDH_NPS_MI_SF_RDV_BDV_2021`.`SAT_SITE_PATIENT_MD_OMOP` ssp
ON hsp.HUB_SITE_PATIENT_SK = ssp.HUB_SITE_PATIENT_SK
WHERE
	sbsm.OMD_CURRENT_RECORD_INDICATOR = 'Y'
	AND sbsm.OMD_DELETED_RECORD_INDICATOR = 'N'
	AND	sbdvbs.OMD_CURRENT_RECORD_INDICATOR = 'Y'
	AND sbdvbs.OMD_DELETED_RECORD_INDICATOR = 'N'
    AND lsbsp.OMD_CURRENT_RECORD_INDICATOR = 'Y'
	AND lsbsp.OMD_DELETED_RECORD_INDICATOR = 'N'
    AND ssp.OMD_CURRENT_RECORD_INDICATOR = 'Y'
	AND ssp.OMD_DELETED_RECORD_INDICATOR = 'N'
)

select * from billing_service_md;

